void optimizeAndTrimCoverImage(vBytes& jpg_vec, std::size_t source_data_size, const ConcealFlags& flags) {
    const bool is_progressive = (source_data_size < PROGRESSIVE_SOURCE_LIMIT) && flags.has_no_option;
    optimizeImage(jpg_vec, is_progressive);

    const auto dqt1 = searchSig(std::span<const Byte>(jpg_vec), DQT1_SIG, DQT_SEARCH_LIMIT);
    const auto dqt2 = searchSig(std::span<const Byte>(jpg_vec), DQT2_SIG, DQT_SEARCH_LIMIT);

    if (!dqt1 && !dqt2) {
        throw std::runtime_error("Image File Error: No DQT segment found (corrupt or unsupported JPG).");
    }

    constexpr auto NPOS = std::numeric_limits<std::size_t>::max();
    const std::size_t dqt_pos = std::min(dqt1.value_or(NPOS), dqt2.value_or(NPOS));
    if (dqt_pos > jpg_vec.size()) {
        throw std::runtime_error("Image File Error: Corrupt DQT metadata.");
    }
    jpg_vec.erase(jpg_vec.begin(), jpg_vec.begin() + static_cast<std::ptrdiff_t>(dqt_pos));
}

void validateCoverImageLimits(std::size_t jpg_size, const ConcealFlags& flags) {
    if (jpg_size > MAX_OPTIMIZED_IMAGE_SIZE) throw std::runtime_error("Image File Error: Cover image file exceeds maximum size limit.");
    if (flags.has_bluesky_option && jpg_size > MAX_OPTIMIZED_BLUESKY_IMG) throw std::runtime_error("File Size Error: Image file exceeds maximum size limit for the Bluesky platform.");
}

[[nodiscard]] std::string validateDataFilename(const fs::path& data_file_path) {
    std::string data_filename = data_file_path.filename().string();
    if (!hasSafeEmbeddedFilename(data_file_path.filename())) {
        throw std::runtime_error(
            "Data File Error: Embedded filename is unsafe. "
            "Filenames may not begin with '.' or '-'.");
    }
    if (data_filename.size() > DATA_FILENAME_MAX_LENGTH) {
        throw std::runtime_error(
            "Data File Error: For compatibility requirements, "
            "length of data filename must not exceed 20 characters.");
    }
    return data_filename;
}

[[nodiscard]] bool shouldBypassCompression(const fs::path& data_file_path, std::size_t source_data_size) {
    return source_data_size > COMPRESS_BYPASS_SIZE && hasFileExtension(data_file_path, {
        ".zip", ".jar", ".rar", ".7z", ".bz2", ".gz", ".xz", ".lz", ".lz4", ".cab", ".rpm", ".deb",
        ".mp4", ".mp3", ".exe", ".jpg", ".jpeg", ".jfif", ".png", ".webp", ".gif", ".ogg", ".flac"
    });
}

